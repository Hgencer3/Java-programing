package day03_escape_sequence;

public class EmployeeInfo {
    public static void main(String[] args){
        System.out.println();
        System.out.println("Company name: \tGoogle");
        System.out.println("Employee:     \tKhalid");
        System.out.println("ID:           \t12345");
        System.out.println("Job Title:    \tSDET");
        System.out.println();
        System.out.println("Company name: \tGoogle\nEmployee:\tKhalid\nID:\t12345\nJob Title:\tSDET");
    }
}
